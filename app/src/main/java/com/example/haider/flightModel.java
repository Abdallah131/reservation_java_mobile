package com.example.haider;

public class flightModel {
    private int flightId = 0;
    private String flightName = "";
    private int flightPassengers = 0;
    private String flightDepature = "";
    private String flightArrival = "";
    private int flightPrice = 0;
    private String departueDate = "";

    public int getFlightId() {
        return flightId;
    }

    public void setFlightId(int flightId) {
        this.flightId = flightId;
    }

    public String getFlightName() {
        return flightName;
    }

    public void setFlightName(String flightName) {
        this.flightName = flightName;
    }

    public int getFlightPassengers() {
        return flightPassengers;
    }

    public void setFlightPassengers(int flightPassengers) {
        this.flightPassengers = flightPassengers;
    }

    public String getFlightDepature() {
        return flightDepature;
    }

    public void setFlightDepature(String flightDepature) {
        this.flightDepature = flightDepature;
    }

    public String getFlightArrival() {
        return flightArrival;
    }

    public void setFlightArrival(String flightArrival) {
        this.flightArrival = flightArrival;
    }

    public int getFlightPrice() {
        return flightPrice;
    }

    public void setFlightPrice(int flightPrice) {
        this.flightPrice = flightPrice;
    }

    public String getDepartueDate() {
        return departueDate;
    }

    public void setDepartueDate(String departueDate) {
        this.departueDate = departueDate;
    }

}
